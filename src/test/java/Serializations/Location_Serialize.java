package Serializations;

public class Location_Serialize {

	//Variable to store values from Json.
		private double lat;
		private double lng;
		
		//Getters & Setters
		public double getLat() {
			return lat;
		}
		public void setLat(double lat) {
			this.lat = lat;
		}
		public double getLng() {
			return lng;
		}
		public void setLng(double lng) {
			this.lng = lng;
		}
		
}
